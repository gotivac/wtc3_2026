    public function saveLog($model)
    {
        
        $change_log = new ChangeLog;
        
        $change_log->attributes = array(
            'model_name' => get_class($model),
            
            'scenario' => $model->scenario,
            'model_id' => $model->id,
            'data' => json_encode($model->attributes)
             
        );
        
        
        $change_log->save();
        return;
    }

    public function rrmdir($path) {
        // Open the source directory to read in files
        $i = new DirectoryIterator($path);
        foreach ($i as $f) {
            if ($f->isFile()) {
                unlink($f->getRealPath());
            } else if (!$f->isDot() && $f->isDir()) {
                $this->rrmdir($f->getRealPath());
            }
        }
        rmdir($path);
    }

    public function date_difference($from_date, $to_date) {
        $start = new DateTime(date("Y-m-d", strtotime(str_replace("/", "-", $from_date))));
        $end = new DateTime(date("Y-m-d", strtotime(str_replace("/", "-", $to_date))));
        return $days = round(($end->format('U') - $start->format('U')) / (60 * 60 * 24));
    }

